/// Абстракция для сервиса аналитики.
///
/// Позволяет отправлять события, устанавливать свойства пользователя
/// и идентификатор пользователя без привязки к конкретной реализации.
abstract class AnalyticsService {
  /// Логирует событие с указанным именем и параметрами.
  ///
  /// [name] - имя события (не должно быть захардкожено в сервисе)
  /// [params] - параметры события (ключ-значение)
  Future<void> logEvent(String name, Map<String, Object?> params);

  /// Устанавливает свойство пользователя.
  ///
  /// [name] - имя свойства
  /// [value] - значение свойства
  Future<void> setUserProperty(String name, String value);

  /// Устанавливает идентификатор пользователя.
  ///
  /// [id] - идентификатор пользователя
  Future<void> setUserId(String id);
}
